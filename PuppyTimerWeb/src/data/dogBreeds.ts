const turkishCharMap: Record<string, string> = {
  "ç": "c",
  "ğ": "g",
  "ı": "i",
  "ö": "o",
  "ş": "s",
  "ü": "u",
  "Ç": "c",
  "Ğ": "g",
  "İ": "i",
  "Ö": "o",
  "Ş": "s",
  "Ü": "u",
};

function normalizeTurkish(text: string): string {
  return text.replace(/[çğıöşüÇĞİÖŞÜ]/g, (ch) => turkishCharMap[ch] ?? ch);
}

export function kopekCinsleriAra(arama: string): string[] {
  if (!arama || arama.trim().length === 0) {
    return [];
  }

  const normalizedSearch = normalizeTurkish(arama.trim()).toLowerCase();

  const results = kopekCinsleri.filter((cins) => {
    const normalizedBreed = normalizeTurkish(cins).toLowerCase();
    const words = normalizedBreed.split(/[\s\-'/]+/);
    return words.some((word) => word.startsWith(normalizedSearch));
  });

  return results.slice(0, 20);
}
